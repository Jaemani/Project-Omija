def _safety_check(path: Path) -> None:
    text = "\n".join(p.read_text(encoding="utf-8") for p in path.glob("*") if p.is_file())
    problems: list[str] = []
    if EMAIL_RE.search(text):
        problems.append("raw email pattern")
    if FORBIDDEN_RE.search(text):
        problems.append("token/API-key pattern")
    lowered = text.lower()
    for forbidden in ("raw_password", "raw_cookie", "raw_token"):
        if forbidden in lowered:
            problems.append(forbidden)
    if problems:
        raise RuntimeError("unsafe foundry measurement output: " + ", ".join(sorted(set(problems))))


def run() -> int:
    data = _read_json(INPUT_JSON)
    measurement = build(data)
    OUT_DIR.mkdir(parents=True, exist_ok=True)
    for old in OUT_DIR.glob("*"):
        if old.is_file() and (old.suffix == ".csv" or old.name in {"measurement.json", "measurement.md", "measurement.html"}):
            old.unlink()
    csv_rows = measurement.pop("_csv_rows")
    for name, rows in csv_rows.items():
        _write_csv(name, rows)
    OUT_JSON.write_text(json.dumps(measurement, indent=2, ensure_ascii=False), encoding="utf-8")
    OUT_MD.write_text(render_markdown(measurement), encoding="utf-8")
    html = render_html(measurement)
    OUT_HTML.write_text(html, encoding="utf-8")
    PUBLIC_HTML.write_text(html, encoding="utf-8")
    _safety_check(OUT_DIR)
    print(f"wrote {OUT_DIR.relative_to(REPO_ROOT)} records={measurement['summary']['input_records']}")
    print("RESULT: OK")
    return 0


if __name__ == "__main__":
    raise SystemExit(run())
